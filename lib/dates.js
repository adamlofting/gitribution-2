function recent() {
  var now = new Date();
  var r = [];
  for (var i = year2014.length - 1; i >= 0; i--) {
    // 86400000 milliseconds in a day
    var compare = new Date(year2014[i]);
    var diff = Math.floor((now - compare) / 86400000);
    if ((diff >= -8) && (diff <= 8)) {
      r.push(year2014[i]);
    }
  }
  return r;
}

